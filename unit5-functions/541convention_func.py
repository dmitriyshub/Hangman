def func(num1, num2):
    """
    the function func() get two numbers and returns the sum value
    :param num1: number value
    :param num2: number value
    :type num1: integer
    :type num2: integer
    :return: the sum of both numbers
    :rtype: integer
    """
    return num1 + num2







